pr = float(input("Введите выручку фирмы "))
iz = float(input("Введите издержки фирмы "))
if pr > iz:
    print(f"Фирма работает с прибылью. Рентабельность выручки составила {pr / iz:.2f}")
    workers = int(input("Введите количество сотрудников фирмы "))
    print(f"Прибыль в расчете на одного сотрудника составила {pr / workers:.2f}")
elif pr == iz:
    print("Фирма работает в ноль")
else:
    print("Фирма работает в убыток")
